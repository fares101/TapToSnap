import UIKit

public extension UIFont {
    static func snapFont1() -> UIFont {
        return UIFont.init(name: "Karla-Regular", size: 48)!
    }
    
    static func snapFont2() -> UIFont {
        return UIFont.init(name: "Karla-Bold", size: 28)!
    }

    static func snapFont3() -> UIFont {
        return UIFont.init(name: "Karla-Regular", size: 24)!
    }

    static func snapFont4() -> UIFont {
        return UIFont.init(name: "Karla-Bold", size: 14)!
    }
}
